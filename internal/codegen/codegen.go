package codegen

import (
	"fmt"
	"io"
	"sort"
	"strings"

	"github.com/befabri/trpcgo/internal/analysis"
	"github.com/befabri/trpcgo/internal/typemap"
)

// ProcEntry is a procedure with pre-converted TypeScript type strings.
type ProcEntry struct {
	Path     string
	ProcType string
	InputTS  string
	OutputTS string
}

// GenerateResult holds the output of a generation pass, allowing
// the caller to also generate Zod schemas from the same data.
type GenerateResult struct {
	Procs []ProcEntry
	Defs  []typemap.TypeDef
}

// Generate writes the TypeScript AppRouter definition to w using static analysis results.
func Generate(w io.Writer, result *analysis.Result, metas map[string]typemap.TypeMeta) (*GenerateResult, error) {
	mapper := typemap.NewMapper(metas)

	var procs []ProcEntry
	for _, p := range result.Procedures {
		inputTS := "void"
		if p.InputType != nil {
			inputTS = mapper.Convert(p.InputType)
		}
		outputTS := mapper.Convert(p.OutputType)

		procs = append(procs, ProcEntry{
			Path:     p.Path,
			ProcType: p.Type,
			InputTS:  inputTS,
			OutputTS: outputTS,
		})
	}

	// Resolve type tokens in proc types (handles collision renaming).
	for i := range procs {
		procs[i].InputTS = mapper.Resolve(procs[i].InputTS)
		procs[i].OutputTS = mapper.Resolve(procs[i].OutputTS)
	}

	defs := mapper.Defs()
	if err := WriteAppRouter(w, procs, defs); err != nil {
		return nil, err
	}
	return &GenerateResult{Procs: procs, Defs: defs}, nil
}

// WriteAppRouter writes the complete TypeScript AppRouter file given
// pre-converted procedure entries and interface definitions.
// Used by both static analysis (Generate) and runtime reflection (GenerateTS).
func WriteAppRouter(w io.Writer, procs []ProcEntry, defs []typemap.TypeDef) error {
	// Header.
	_, _ = fmt.Fprintln(w, "// Code generated by trpcgo. DO NOT EDIT.")
	_, _ = fmt.Fprintln(w)

	// Determine which procedure types are used.
	usedTypes := map[string]bool{}
	for _, p := range procs {
		usedTypes[p.ProcType] = true
	}

	// Import from @trpc/server (only include types actually used).
	var imports []string
	imports = append(imports, "TRPCRouterDef", "TRPCRouterCaller")
	if usedTypes["query"] {
		imports = append(imports, "TRPCQueryProcedure")
	}
	if usedTypes["mutation"] {
		imports = append(imports, "TRPCMutationProcedure")
	}
	if usedTypes["subscription"] {
		imports = append(imports, "TRPCSubscriptionProcedure")
	}
	if len(procs) > 0 {
		imports = append(imports, "inferRouterInputs", "inferRouterOutputs")
	}
	sort.Strings(imports)

	_, _ = fmt.Fprintln(w, "import type {")
	for _, name := range imports {
		_, _ = fmt.Fprintf(w, "  %s,\n", name)
	}
	_, _ = fmt.Fprintln(w, "} from '@trpc/server';")
	_, _ = fmt.Fprintln(w)

	// Type definitions (interfaces, unions, aliases).
	for _, def := range defs {
		writeJSDoc(w, def.Comment, "")
		switch def.Kind {
		case typemap.TypeDefInterface:
			writeInterface(w, def)
		case typemap.TypeDefUnion:
			writeUnion(w, def)
		case typemap.TypeDefAlias:
			writeAlias(w, def)
		}
		_, _ = fmt.Fprintln(w)
	}

	// Procedure type aliases (only emit types that are used).
	if usedTypes["query"] {
		_, _ = fmt.Fprintln(w, `type $Query<TInput, TOutput> = TRPCQueryProcedure<{ input: TInput; output: TOutput; meta: object }>;`)
	}
	if usedTypes["mutation"] {
		_, _ = fmt.Fprintln(w, `type $Mutation<TInput, TOutput> = TRPCMutationProcedure<{ input: TInput; output: TOutput; meta: object }>;`)
	}
	if usedTypes["subscription"] {
		_, _ = fmt.Fprintln(w, `type $Subscription<TInput, TOutput> = TRPCSubscriptionProcedure<{ input: TInput; output: TOutput; meta: object }>;`)
	}
	if len(procs) > 0 {
		_, _ = fmt.Fprintln(w)
	}

	// Build procedure tree from dot-separated paths.
	tree := buildProcTree(procs, func(p ProcEntry) procLeaf {
		return procLeaf{procType: p.ProcType, inputTS: p.InputTS, outputTS: p.OutputTS}
	})

	_, _ = fmt.Fprint(w, "type AppRouterRecord = ")
	writeTreeNodes(w, tree, 0, func(l procLeaf) string {
		helper := "$Query"
		switch l.procType {
		case "mutation":
			helper = "$Mutation"
		case "subscription":
			helper = "$Subscription"
		}
		return fmt.Sprintf("%s<%s, %s>", helper, l.inputTS, l.outputTS)
	})
	_, _ = fmt.Fprintln(w, ";")
	_, _ = fmt.Fprintln(w)

	// AppRouter using @trpc/server types.
	_, _ = fmt.Fprint(w, `type $ErrorShape = { code: number; message: string; data: { code: string; httpStatus: number; path?: string } };
type $RootTypes = { ctx: object; meta: object; errorShape: $ErrorShape; transformer: false };

export type AppRouter = {
  _def: TRPCRouterDef<$RootTypes, AppRouterRecord>;
  createCaller: TRPCRouterCaller<$RootTypes, AppRouterRecord>;
};
`)
	_, _ = fmt.Fprintln(w)

	// RouterInputs / RouterOutputs via tRPC inference.
	if len(procs) > 0 {
		_, _ = fmt.Fprintln(w, "export type RouterInputs = inferRouterInputs<AppRouter>;")
		_, _ = fmt.Fprintln(w)
		_, _ = fmt.Fprintln(w, "export type RouterOutputs = inferRouterOutputs<AppRouter>;")
	}

	return nil
}

func writeJSDoc(w io.Writer, comment, indent string) {
	if comment == "" {
		return
	}
	lines := strings.Split(comment, "\n")
	if len(lines) == 1 {
		_, _ = fmt.Fprintf(w, "%s/** %s */\n", indent, lines[0])
		return
	}
	_, _ = fmt.Fprintf(w, "%s/**\n", indent)
	for _, line := range lines {
		if line == "" {
			_, _ = fmt.Fprintf(w, "%s *\n", indent)
		} else {
			_, _ = fmt.Fprintf(w, "%s * %s\n", indent, line)
		}
	}
	_, _ = fmt.Fprintf(w, "%s */\n", indent)
}

func writeInterface(w io.Writer, def typemap.TypeDef) {
	typeParams := ""
	if len(def.TypeParams) > 0 {
		typeParams = "<" + strings.Join(def.TypeParams, ", ") + ">"
	}
	extendsClause := ""
	if len(def.Extends) > 0 {
		extendsClause = " extends " + strings.Join(def.Extends, ", ")
	}
	_, _ = fmt.Fprintf(w, "export interface %s%s%s {\n", def.Name, typeParams, extendsClause)
	for _, f := range def.Fields {
		writeJSDoc(w, f.Comment, "  ")
		opt := ""
		if f.Optional {
			opt = "?"
		}
		prefix := ""
		if f.Readonly {
			prefix = "readonly "
		}
		_, _ = fmt.Fprintf(w, "  %s%s%s: %s;\n", prefix, typemap.QuotePropName(f.Name), opt, f.Type)
	}
	_, _ = fmt.Fprintln(w, "}")
}

func writeUnion(w io.Writer, def typemap.TypeDef) {
	_, _ = fmt.Fprintf(w, "export type %s = %s;\n", def.Name, strings.Join(def.UnionMembers, " | "))
}

func writeAlias(w io.Writer, def typemap.TypeDef) {
	_, _ = fmt.Fprintf(w, "export type %s = %s;\n", def.Name, def.AliasOf)
}

// procLeaf holds the data for a procedure tree leaf node.
type procLeaf struct {
	procType string
	inputTS  string
	outputTS string
}

// treeNode is a generic tree node parameterized by leaf data type.
type treeNode[L any] struct {
	children map[string]*treeNode[L]
	isLeaf   bool
	leaf     L
}

// buildProcTree builds a tree from dot-separated procedure paths,
// extracting leaf data from each ProcEntry via leafFn.
func buildProcTree[L any](procs []ProcEntry, leafFn func(ProcEntry) L) *treeNode[L] {
	root := &treeNode[L]{children: make(map[string]*treeNode[L])}
	for _, p := range procs {
		parts := strings.Split(p.Path, ".")
		node := root
		for i, part := range parts {
			child, ok := node.children[part]
			if !ok {
				child = &treeNode[L]{children: make(map[string]*treeNode[L])}
				node.children[part] = child
			}
			if i == len(parts)-1 {
				child.isLeaf = true
				child.leaf = leafFn(p)
			}
			node = child
		}
	}
	return root
}

// writeTreeNodes recursively writes a tree as TypeScript object notation,
// using renderLeaf to format each leaf node.
func writeTreeNodes[L any](w io.Writer, node *treeNode[L], indent int, renderLeaf func(L) string) {
	keys := make([]string, 0, len(node.children))
	for k := range node.children {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	prefix := strings.Repeat("  ", indent)
	_, _ = fmt.Fprintln(w, "{")
	for _, key := range keys {
		child := node.children[key]
		qk := typemap.QuotePropName(key)
		if child.isLeaf && len(child.children) > 0 {
			// Path is both a leaf and a namespace — emit intersection type.
			_, _ = fmt.Fprintf(w, "%s  %s: %s & ", prefix, qk, renderLeaf(child.leaf))
			writeTreeNodes(w, child, indent+1, renderLeaf)
			_, _ = fmt.Fprintln(w, ";")
		} else if child.isLeaf {
			_, _ = fmt.Fprintf(w, "%s  %s: %s;\n", prefix, qk, renderLeaf(child.leaf))
		} else {
			_, _ = fmt.Fprintf(w, "%s  %s: ", prefix, qk)
			writeTreeNodes(w, child, indent+1, renderLeaf)
			_, _ = fmt.Fprintln(w, ";")
		}
	}
	_, _ = fmt.Fprintf(w, "%s}", prefix)
}

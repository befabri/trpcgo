func TestWriteEnumsEmptyDefs(t *testing.T) {
	output := writeEnums(t, nil)

	if !strings.HasPrefix(output, "// Code generated by trpcgo. DO NOT EDIT.") {
		t.Errorf("expected header, got:\n%s", output)
	}
	if strings.Contains(output, "export const") {
		t.Errorf("no defs should yield no objects:\n%s", output)
	}
}

func TestWriteEnumsMultipleStringUnions(t *testing.T) {
	defs := []typemap.TypeDef{
		{Name: "Role", Kind: typemap.TypeDefUnion, UnionMembers: []string{`"admin"`}},
		{Name: "Status", Kind: typemap.TypeDefUnion, UnionMembers: []string{`"on"`, `"off"`}},
	}
	output := writeEnums(t, defs)

	if !strings.Contains(output, "export const RoleEnum = {") || !strings.Contains(output, "export const StatusEnum = {") {
		t.Errorf("both enums should be emitted:\n%s", output)
	}
	if n := strings.Count(output, "} as const;"); n != 2 {
		t.Errorf("expected two closed objects, found %d:\n%s", n, output)
	}
}

func TestWriteEnumsDeduplicatesRepeatedValues(t *testing.T) {
	defs := []typemap.TypeDef{
		{Name: "Color", Kind: typemap.TypeDefUnion, UnionMembers: []string{`"red"`, `"red"`, `"blue"`}},
	}
	output := writeEnums(t, defs)

	if n := strings.Count(output, `red: "red",`); n != 1 {
		t.Errorf("duplicate value should be emitted once, found %d:\n%s", n, output)
	}
	if !strings.Contains(output, `blue: "blue",`) {
		t.Errorf("missing distinct value blue:\n%s", output)
	}
}


func TestWriteEnumsSkipsEmptyUnionMembers(t *testing.T) {
	defs := []typemap.TypeDef{
		{Name: "Empty", Kind: typemap.TypeDefUnion, UnionMembers: nil},
		{Name: "Role", Kind: typemap.TypeDefUnion, UnionMembers: []string{`"a"`}},
	}
	output := writeEnums(t, defs)

	if strings.Contains(output, "EmptyEnum") {
		t.Errorf("a union with no members should be skipped:\n%s", output)
	}
	if !strings.Contains(output, "RoleEnum") {
		t.Errorf("a populated union should still emit:\n%s", output)
	}
}


func writeEnums(t *testing.T, defs []typemap.TypeDef) string {
	t.Helper()
	var buf bytes.Buffer
	if err := codegen.WriteEnums(&buf, defs); err != nil {
		t.Fatal(err)
	}
	return buf.String()
}

type failingWriter struct {
	err error
}

func (w failingWriter) Write([]byte) (int, error) {
	return 0, w.err
}

func TestWriteEnumsEndToEnd(t *testing.T) {
	dir := testdataDir("crosspkg")
	result, err := analysis.Analyze([]string{"."}, dir)
	if err != nil {
		t.Fatal(err)
	}
	var trpcBuf bytes.Buffer
	gen, err := codegen.Generate(&trpcBuf, result, result.TypeMetas)
	if err != nil {
		t.Fatal(err)
	}

	var buf bytes.Buffer
	if err := codegen.WriteEnums(&buf, gen.Defs); err != nil {
		t.Fatal(err)
	}
	output := buf.String()

	for _, want := range []string{
		"export const RoleEnum = {",
		`viewer: "viewer",`,
		"export const StatusEnum = {",
		`PENDING: "PENDING",`,
		"export const PhaseEnum = {",
		`queued: "queued",`,
	} {
		if !strings.Contains(output, want) {
			t.Errorf("end-to-end output missing %q:\n%s", want, output)
		}
	}

	if strings.Contains(output, "DurationEnum") {
		t.Errorf("stdlib Duration leaked an enum object:\n%s", output)
	}
}

func TestWriteEnumsReturnsWriteError(t *testing.T) {
	writeErr := errors.New("write failed")
	err := codegen.WriteEnums(failingWriter{err: writeErr}, []typemap.TypeDef{
		{Name: "Role", Kind: typemap.TypeDefUnion, UnionMembers: []string{`"admin"`}},
	})
	if !errors.Is(err, writeErr) {
		t.Fatalf("WriteEnums error = %v, want wrapping %v", err, writeErr)
	}
}

func TestWriteEnumsStringUnion(t *testing.T) {
	defs := []typemap.TypeDef{
		{
			Name:         "Role",
			Kind:         typemap.TypeDefUnion,
			UnionMembers: []string{`"viewer"`, `"admin"`, `"owner"`},
		},
	}
	output := writeEnums(t, defs)

	want := "export const RoleEnum = {\n  viewer: \"viewer\",\n  admin: \"admin\",\n  owner: \"owner\",\n} as const;"
	if !strings.Contains(output, want) {
		t.Errorf("string enum object mismatch.\nwant:\n%s\ngot:\n%s", want, output)
	}
	if !strings.HasPrefix(output, "// Code generated by trpcgo. DO NOT EDIT.") {
		t.Errorf("missing DO NOT EDIT header.\ngot:\n%s", output)
	}
}

func TestWriteEnumsOrderMatchesUnion(t *testing.T) {
	defs := []typemap.TypeDef{
		{
			Name:         "Status",
			Kind:         typemap.TypeDefUnion,
			UnionMembers: []string{`"PENDING"`, `"RUNNING"`, `"DONE"`, `"FAILED"`},
		},
	}
	output := writeEnums(t, defs)

	idx := []int{
		strings.Index(output, "PENDING:"),
		strings.Index(output, "RUNNING:"),
		strings.Index(output, "DONE:"),
		strings.Index(output, "FAILED:"),
	}
	for i := 1; i < len(idx); i++ {
		if idx[i-1] < 0 || idx[i] < 0 || idx[i-1] >= idx[i] {
			t.Errorf("members out of order: %v\noutput:\n%s", idx, output)
		}
	}
}


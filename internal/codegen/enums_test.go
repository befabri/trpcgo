func TestWriteEnumsMixedDefs(t *testing.T) {
	defs := []typemap.TypeDef{
		{Name: "User", Kind: typemap.TypeDefInterface},
		{Name: "Role", Kind: typemap.TypeDefUnion, UnionMembers: []string{`"admin"`, `"viewer"`}},
		{Name: "Email", Kind: typemap.TypeDefAlias, AliasOf: "string"},
		{Name: "Priority", Kind: typemap.TypeDefUnion, UnionMembers: []string{"1", "2"}},
	}
	output := writeEnums(t, defs)

	if !strings.Contains(output, "export const RoleEnum = {") {
		t.Errorf("string union should be emitted:\n%s", output)
	}
	for _, notWant := range []string{"UserEnum", "EmailEnum", "PriorityEnum"} {
		if strings.Contains(output, notWant) {
			t.Errorf("unexpected %s emitted:\n%s", notWant, output)
		}
	}
}

